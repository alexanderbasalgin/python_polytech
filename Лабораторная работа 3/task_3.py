# TODO  Напишите функцию count_letters
def count_letters(string):
    dic = {}
    for i in string:
        i = i.lower()
        if i.isalpha():
            if i in dic:
                dic[i] += 1
            else:
                dic[i] = 1
    return dic


# TODO Напишите функцию calculate_frequency
def calculate_frequency(dic):
    summ = sum(list(dic.values()))
    new_dic = {}
    for letter, count in dic.items():
        new_dic[letter] = count/summ

    return new_dic


